#[tokio::test]
async fn grpc_web_leading_metadata_header() {
    let address = spawn_web_server().await;
    let desc = descriptor_file();
    let config = web_config(
        &address,
        &desc.path().to_string_lossy(),
        CompressionMode::None,
    );

    let resp = execute_web(
        &config,
        "echo.EchoService",
        "SayHello",
        json!({ "message": "World" }),
    )
    .await
    .expect("grpc-web unary call");

    // Leading metadata set by the server surfaces as an HTTP header over grpc-web.
    let engine = AssertionEngine::with_registry(Arc::new(PluginManager::new()));
    let response = resp.messages.first().cloned().unwrap_or(json!({}));
    let header = engine
        .evaluate(
            "@header(\"x-custom-header\") == \"custom-value\"",
            &response,
            Some(&resp.headers),
            Some(&resp.trailers),
        )
        .expect("evaluate @header");
    assert_eq!(header, AssertionResult::Pass, "headers: {:?}", resp.headers);
}

