#[tokio::test]
async fn connect_unary_error_details() {
    let address = spawn_connect_server().await;
    let config = connect_config(&address, None);

    let resp = execute_web(
        &config,
        "echo.EchoService",
        "SayHello",
        json!({ "message": "boom-details" }),
    )
    .await
    .expect("connect unary error-details call");

    let error = resp.error.expect("expected a Connect error");
    assert_eq!(error.code, 3, "invalid_argument = 3");

    // For HTTP transports the Connect `details` array is carried as raw JSON-array
    // bytes; assert it round-trips with the type + value intact.
    let details: Value =
        serde_json::from_slice(&error.details).expect("details should be a JSON array");
    assert_eq!(details[0]["type"], json!("google.rpc.ErrorInfo"));
    assert_eq!(details[0]["value"], json!(DETAIL_VALUE_B64));
}

#[tokio::test]
async fn connect_unary_leading_metadata_header() {
    let address = spawn_connect_server().await;
    let config = connect_config(&address, None);

    let resp = execute_web(
        &config,
        "echo.EchoService",
        "SayHello",
        json!({ "message": "World" }),
    )
    .await
    .expect("connect unary call");

    let response = resp.messages.first().cloned().unwrap_or(json!({}));
    let header = engine()
        .evaluate(
            "@header(\"x-custom-header\") == \"custom-value\"",
            &response,
            Some(&resp.headers),
            Some(&resp.trailers),
        )
        .expect("evaluate @header");
    assert_eq!(header, AssertionResult::Pass, "headers: {:?}", resp.headers);
}

#[tokio::test]
async fn connect_server_streaming_multiple() {
    let address = spawn_connect_server().await;
    let desc = descriptor_file();
    let config = connect_config(&address, Some(&desc.path().to_string_lossy()));

    let resp = execute_web(
        &config,
        "echo.EchoService",
        "ServerStream",
        json!({ "prefix": "item", "count": 3 }),
    )
    .await
    .expect("connect server-streaming call");

    assert!(resp.error.is_none(), "unexpected error: {:?}", resp.error);
    assert_eq!(resp.messages.len(), 3, "messages: {:?}", resp.messages);
    assert_eq!(resp.messages[0]["message"], json!("item #0"));
    assert_eq!(resp.messages[2]["message"], json!("item #2"));

    // The EndStreamMessage `metadata` surfaces as trailers.
    let response = resp.messages.first().cloned().unwrap_or(json!({}));
    let trailer = engine()
        .evaluate(
            "@trailer(\"x-stream-trailer\") == \"trailer-value\"",
            &response,
            Some(&resp.headers),
            Some(&resp.trailers),
        )
        .expect("evaluate @trailer");
    assert_eq!(
        trailer,
        AssertionResult::Pass,
        "trailers: {:?}",
        resp.trailers
    );
}

#[tokio::test]
async fn connect_streaming_end_stream_error_and_trailers() {
    let address = spawn_connect_server().await;
    let desc = descriptor_file();
    let config = connect_config(&address, Some(&desc.path().to_string_lossy()));

    let resp = execute_web(
        &config,
        "echo.EchoService",
        "ServerStream",
        json!({ "prefix": "err", "count": 0 }),
    )
    .await
    .expect("connect streaming error call");

    // Data frames delivered before the failing end-of-stream frame are still seen.
    assert_eq!(resp.messages.len(), 2, "messages: {:?}", resp.messages);

    let error = resp.error.expect("expected an end-stream error");
    assert_eq!(error.code, 8, "resource_exhausted = 8");
    assert!(
        error.message.contains("stream blew up"),
        "message: {}",
        error.message
    );

    // Streaming errors also carry `details` (raw JSON-array bytes).
    let details: Value =
        serde_json::from_slice(&error.details).expect("details should be a JSON array");
    assert_eq!(details[0]["type"], json!("google.rpc.RetryInfo"));

    // Trailers from the EndStreamMessage `metadata` drive `@trailer(...)`.
    let response = resp.messages.first().cloned().unwrap_or(json!({}));
    let present = engine()
        .evaluate(
            "@trailer(\"x-stream-trailer\")",
            &response,
            Some(&resp.headers),
            Some(&resp.trailers),
        )
        .expect("evaluate @trailer present");
    assert_eq!(
        present,
        AssertionResult::Pass,
        "trailers: {:?}",
        resp.trailers
    );

    let value = engine()
        .evaluate(
            "@trailer(\"x-stream-trailer\") == \"trailer-value\"",
            &response,
            Some(&resp.headers),
            Some(&resp.trailers),
        )
        .expect("evaluate @trailer value");
    assert_eq!(
        value,
        AssertionResult::Pass,
        "trailers: {:?}",
        resp.trailers
    );
}

